# endure - make html MORE easier
import sys
from os import getcwd

ENDURE_CV = 1
ENDURE_ARGS = sys.argv[1:]
ENDURE_DOC = ""
backslashn = '\n'
whereto = ""
ENDURE_TEMP = ""

def loadf(mod):
    with open(mod) as f:
        content = f.readlines()
        content = [x.strip() for x in content]
    for command in content:
        exec(command)
def write_doc():
    o = open(whereto, "w+")
    o.write(ENDURE_DOC)
    o.close()
def generate():
    ENDURE_DOC = "<html>\n"
    ENDURE_DOC = ENDURE_DOC+"<!--Autogenerated html with endure, DO NOT EDIT -->"+backslashn
    ENDURE_DOC = ENDURE_DOC+ENDURE_TEMP
    ENDURE_DOC = ENDURE_DOC + "</html>\n"
def paragraph(text):
    ENDURE_TEMP = ENDURE_TEMP + "<p>"+text+"</p>"+backslashn
def dino():
    if "-o" in ENDURE_ARGS:
        whereto = getcwd+"/"+ENDURE_ARGS["-o".index(ENDURE_ARGS)+1]
        endfile = getcwd+"/"+ENDURE_ARGS[0]
    else:
        whereto = getcwd()+'/'+"a.out"
        endfile = getcwd+"/"+ENDURE_ARGS[0]
        generate()
if __name__ == "__main__":
    dino()